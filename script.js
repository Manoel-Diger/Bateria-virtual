// Configura√ß√£o inicial
let isRecording = false;
let recordedBeats = [];
let startTime = 0;
let currentVolume = 0.7;

// Elementos do DOM
const drumPads = document.querySelectorAll('.drum-pad');
const recordBtn = document.getElementById('recordBtn');
const playBtn = document.getElementById('playBtn');
const clearBtn = document.getElementById('clearBtn');
const volumeControl = document.getElementById('volumeControl');
const keyDisplay = document.getElementById('keyDisplay');
const kitSelect = document.getElementById('kitSelect');

// Mapeamento de teclas
const keyMap = {
    'KeyQ': 'q', 'KeyW': 'w', 'KeyE': 'e',
    'KeyA': 'a', 'KeyS': 's', 'KeyD': 'd',
    'KeyZ': 'z', 'KeyX': 'x', 'KeyC': 'c'
};

// üîì SOLU√á√ÉO DEFINITIVA PARA √ÅUDIO EM DISPOSITIVOS M√ìVEIS
let mobileAudioEnabled = false;
let audioPool = {};

// Fun√ß√£o para detectar dispositivo m√≥vel
function isMobileDevice() {
    return /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) || 
           ('ontouchstart' in window) || 
           (navigator.maxTouchPoints > 0);
}

// Criar pool de √°udios para mobile
function createAudioPool() {
    const keys = ['q', 'w', 'e', 'a', 's', 'd', 'z', 'x', 'c'];
    
    keys.forEach(key => {
        audioPool[key] = new Audio(`sounds/key${key}.wav`);
        audioPool[key].preload = 'auto';
        audioPool[key].volume = currentVolume;
        audioPool[key].load();
    });
    
    console.log('üéµ Pool de √°udios criado para dispositivos m√≥veis');
}

// Fun√ß√£o para habilitar √°udio em dispositivos m√≥veis
function enableMobileAudio() {
    if (mobileAudioEnabled) return;
    
    console.log('üîì Habilitando √°udio para dispositivos m√≥veis...');
    
    // Criar pool de √°udios se for mobile
    if (isMobileDevice()) {
        createAudioPool();
        
        // Tocar todos os √°udios silenciosamente para "acordar" o sistema
        Object.keys(audioPool).forEach(key => {
            const audio = audioPool[key];
            audio.volume = 0;
            
            const playPromise = audio.play();
            if (playPromise !== undefined) {
                playPromise
                    .then(() => {
                        audio.pause();
                        audio.currentTime = 0;
                        audio.volume = currentVolume;
                        console.log(`‚úÖ √Åudio ${key} habilitado para mobile`);
                    })
                    .catch(error => {
                        console.log(`‚ùå Erro ao habilitar ${key} no mobile:`, error);
                    });
            }
        });
    } else {
        // Desktop - usar m√©todo original
        drumPads.forEach(pad => {
            const audio = pad.querySelector('audio');
            if (audio) {
                audio.volume = 0;
                const playPromise = audio.play();
                
                if (playPromise !== undefined) {
                    playPromise
                        .then(() => {
                            audio.pause();
                            audio.currentTime = 0;
                            audio.volume = currentVolume;
                        })
                        .catch(() => {});
                }
            }
        });
    }
    
    mobileAudioEnabled = true;
    console.log('‚úÖ √Åudio habilitado com sucesso!');
}

// Event listener espec√≠fico para mobile - usando 'click' em vez de 'touchstart'
document.addEventListener('click', enableMobileAudio, { once: true });
document.addEventListener('touchend', enableMobileAudio, { once: true });
document.addEventListener('keydown', enableMobileAudio, { once: true });

// Fun√ß√£o principal para tocar som - OTIMIZADA PARA MOBILE
function playSound(key) {
    const pad = document.querySelector(`.drum-pad[data-key="${key}"]`);
    if (!pad) {
        console.log(`Pad n√£o encontrado para a tecla: ${key}`);
        return;
    }

    let audio;

    // Usar pool de √°udios se for dispositivo m√≥vel
    if (isMobileDevice() && audioPool[key]) {
        audio = audioPool[key];
    } else {
        // Desktop - usar m√©todo original
        audio = pad.querySelector('audio');
        
        // Corre√ß√£o espec√≠fica para a tecla "s"
        if (key === 's') {
            audio = new Audio('sounds/keys.wav');
        }
    }

    if (!audio) {
        console.log(`√Åudio n√£o encontrado no pad: ${key}`);
        return;
    }

    console.log(`Tocando som: ${key} - ${audio.src || 'pool audio'}`);

    // Resetar √°udio
    audio.currentTime = 0;
    audio.volume = currentVolume;
    
    // Reproduzir √°udio
    const playPromise = audio.play();
    
    if (playPromise !== undefined) {
        playPromise
            .then(() => {
                console.log(`‚úÖ Som tocado com sucesso: ${key}`);
            })
            .catch(error => {
                console.error(`‚ùå Erro ao reproduzir √°udio ${key}:`, error);
                
                // Tentar novamente se n√£o foi habilitado ainda
                if (!mobileAudioEnabled) {
                    enableMobileAudio();
                    setTimeout(() => playSound(key), 200);
                }
            });
    }

    pad.classList.add('active');
    setTimeout(() => {
        pad.classList.remove('active');
    }, 150);

    showKeyDisplay(key.toUpperCase());

    if (isRecording) {
        const timestamp = Date.now() - startTime;
        recordedBeats.push({ key, timestamp });
    }
}

// Fun√ß√£o para mostrar a tecla pressionada na tela
function showKeyDisplay(key) {
    keyDisplay.textContent = key;
    keyDisplay.classList.add('show');
    
    setTimeout(() => {
        keyDisplay.classList.remove('show');
    }, 800);
}

// Event listeners para teclas do teclado
document.addEventListener('keydown', (e) => {
    const key = keyMap[e.code];
    if (key) {
        e.preventDefault();
        playSound(key);
    }
});

// Event listeners para cliques nos pads
drumPads.forEach(pad => {
    pad.addEventListener('click', () => {
        const key = pad.getAttribute('data-key');
        playSound(key);
    });
});

// Controle de volume
volumeControl.addEventListener('input', (e) => {
    currentVolume = e.target.value / 100;
    console.log(`Volume alterado para: ${Math.round(currentVolume * 100)}%`);
    
    // Atualizar volume nos √°udios
    if (isMobileDevice() && audioPool) {
        Object.keys(audioPool).forEach(key => {
            audioPool[key].volume = currentVolume;
        });
    } else {
        drumPads.forEach(pad => {
            const audio = pad.querySelector('audio');
            if (audio) {
                audio.volume = currentVolume;
            }
        });
    }
});

// Grava√ß√£o
recordBtn.addEventListener('click', () => {
    if (!isRecording) {
        isRecording = true;
        recordedBeats = [];
        startTime = Date.now();
        recordBtn.textContent = '‚èπÔ∏è Parar';
        recordBtn.classList.add('recording');
        playBtn.disabled = true;
        console.log('üî¥ Grava√ß√£o iniciada');
    } else {
        isRecording = false;
        recordBtn.textContent = 'üî¥ Gravar';
        recordBtn.classList.remove('recording');
        playBtn.disabled = recordedBeats.length === 0;
        console.log(`‚èπÔ∏è Grava√ß√£o parada. ${recordedBeats.length} beats gravados`);
    }
});

// Reprodu√ß√£o
playBtn.addEventListener('click', () => {
    if (recordedBeats.length === 0) return;

    playBtn.disabled = true;
    recordBtn.disabled = true;
    playBtn.textContent = '‚ñ∂Ô∏è Reproduzindo...';
    console.log(`‚ñ∂Ô∏è Reproduzindo ${recordedBeats.length} beats`);

    recordedBeats.forEach(beat => {
        setTimeout(() => {
            playSound(beat.key);
        }, beat.timestamp);
    });

    const lastBeatTime = recordedBeats[recordedBeats.length - 1].timestamp;
    setTimeout(() => {
        playBtn.disabled = false;
        recordBtn.disabled = false;
        playBtn.textContent = '‚ñ∂Ô∏è Reproduzir';
        console.log('‚ñ∂Ô∏è Reprodu√ß√£o finalizada');
    }, lastBeatTime + 1000);
});

// Limpar grava√ß√£o
clearBtn.addEventListener('click', () => {
    recordedBeats = [];
    playBtn.disabled = true;
    isRecording = false;
    recordBtn.textContent = 'üî¥ Gravar';
    recordBtn.classList.remove('recording');
    playBtn.textContent = '‚ñ∂Ô∏è Reproduzir';
    console.log('üóëÔ∏è Grava√ß√£o limpa');
});

